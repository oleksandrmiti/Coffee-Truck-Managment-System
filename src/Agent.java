/**
 * This is a superclass that handle an ID for Customers and Products
 */
public class Agent {

//---------------------------------------
//	Fields
//---------------------------------------
private final int id;

//---------------------------------------
//	Constructor
//---------------------------------------
public Agent(int _id){
    // 1.0. Initialise the field
    this.id = _id;
}

//---------------------------------------
//	GET METHODS
//---------------------------------------

    //---------------------------------------
    //	getID
    //---------------------------------------

    /**
     * This method returns ID
     * @return id
     */
    protected int getID(){
        return this.id;
    }

}